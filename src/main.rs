fn add_imposter(new_data: &mut Vec<u8>, id: usize) {
    let imposter = IMPOSTORS[id];

    for i in imposter.chars() {
        let mut buf = [0; 5];

        let x = i.encode_utf8(&mut buf);

        for i in x.as_bytes() {
            new_data.push(*i);
        }
    }
}

